# Implementation of stack using list in Python 
# Stack is "LIFO(Last In First Out)"

# append method is used to add an item on top of stack 
# pop method is used to get the topmost item from stack

stack = ['first','second','third','fourth'] 
stack.append("abc") 
stack.append("def") 

print(stack) 
print(stack.pop()) 
print(stack) 
print(stack.pop()) 
print(stack) 